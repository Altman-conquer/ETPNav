import numpy as np


def find_first_nonzero_elem_per_row(mat):
    H, W = mat.shape
    x = np.linspace(0, W - 1, W)
    y = np.linspace(0, H - 1, H)
    xv, yv = np.meshgrid(x, y)

    xv[mat == 0] = 0
    min_idx_nonzero_per_row = np.max(xv, axis=1).astype(int)

    yv = yv[:, 0].astype(int)

    result = mat[yv, min_idx_nonzero_per_row]
    return result


